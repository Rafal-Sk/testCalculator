package myqualityassurance;

public class Operations {


    public long additions(long a, long b)
    {
        return a+b;

    }

    public long substraction(long a, long b)
    {
        return a-b;

    }

    public long multiplication(long a, long b)
    {
        return a*b;

    }

    public long division(long a, long b)
    {
        return a/b;

    }

}
